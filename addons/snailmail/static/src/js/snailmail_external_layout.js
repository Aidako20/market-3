//Changeaddressfont-sizeifneeded
document.addEventListener('DOMContentLoaded',function(evt){
    varrecipientAddress=document.querySelector(".address.row>div[name='address']>address");
    letbaseSize=120;
    if(!recipientAddress){
        recipientAddress=document.querySelector("div.row.fallback_header>div.col-5.offset-7>div:first-child");
    }
    varstyle=window.getComputedStyle(recipientAddress,null);
    varheight=parseFloat(style.getPropertyValue('height'));
    varfontSize=parseFloat(style.getPropertyValue('font-size'));
    recipientAddress.style.fontSize=(baseSize/(height/fontSize))+'px';
});
