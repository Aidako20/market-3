flectra.define('web.UserMenu',function(require){
"usestrict";

/**
 *Thiswidgetisappendedbythewebclienttotherightofthenavbar.
 *Itdisplaystheavatarandthenameoftheloggeduser(andoptionallythe
 *dbname,indebugmode).
 *Ifclicked,itopensadropdownallowingtheusertoperformactionslike
 *editingitspreferences,accessingthedocumentation,loggingout...
 */

varconfig=require('web.config');
varcore=require('web.core');
varframework=require('web.framework');
varDialog=require('web.Dialog');
varWidget=require('web.Widget');

var_t=core._t;
varQWeb=core.qweb;

varUserMenu=Widget.extend({
    template:'UserMenu',

    /**
     *@override
     *@returns{Promise}
     */
    start:function(){
        varself=this;
        varsession=this.getSession();
        this.$el.on('click','[data-menu]',function(ev){
            ev.preventDefault();
            varmenu=$(this).data('menu');
            self['_onMenu'+menu.charAt(0).toUpperCase()+menu.slice(1)]();
        });
        returnthis._super.apply(this,arguments).then(function(){
            var$avatar=self.$('.oe_topbar_avatar');
            if(!session.uid){
                $avatar.attr('src',$avatar.data('default-src'));
                returnPromise.resolve();
            }
            vartopbar_name=session.name;
            if(config.isDebug()){
                topbar_name=_.str.sprintf("%s(%s)",topbar_name,session.db);
            }
            self.$('.oe_topbar_name').text(topbar_name);
            varavatar_src=session.url('/web/image',{
                model:'res.users',
                field:'image_128',
                id:session.uid,
            });
            $avatar.attr('src',avatar_src);
        });
    },

    //--------------------------------------------------------------------------
    //Handlers
    //--------------------------------------------------------------------------

    /**
     *@private
     */
    _onMenuAccount:function(){
        varself=this;
        this.trigger_up('clear_uncommitted_changes',{
            callback:function(){
                self._rpc({route:'/web/session/account'})
                    .then(function(url){
                        framework.redirect(url);
                    })
                    .guardedCatch(function(result,ev){
                        ev.preventDefault();
                        framework.redirect('https://accounts.flectrahq.com/account');
                    });
            },
        });
    },
    /**
     *@private
     */
    _onMenuDocumentation:function(){
        window.open('https://doc.flectrahq.com/2.0','_blank');
    },
    /**
     *@private
     */
    _onMenuLogout:function(){
        this.trigger_up('clear_uncommitted_changes',{
            callback:this.do_action.bind(this,'logout'),
        });
    },
    /**
     *@private
     */
    _onMenuSettings:function(){
        varself=this;
        varsession=this.getSession();
        this.trigger_up('clear_uncommitted_changes',{
            callback:function(){
                self._rpc({
                        model:"res.users",
                        method:"action_get"
                    })
                    .then(function(result){
                        result.res_id=session.uid;
                        self.do_action(result);
                    });
            },
        });
    },
    /**
     *@private
     */
    _onMenuSupport:function(){
        window.open('https://www.flectrahq.com/buy','_blank');
    },
    /**
     *@private
     */
    _onMenuShortcuts:function(){
        newDialog(this,{
            size:'large',
            dialogClass:'o_act_window',
            title:_t("KeyboardShortcuts"),
            $content:$(QWeb.render("UserMenu.shortcuts"))
        }).open();
    },
});

returnUserMenu;

});
