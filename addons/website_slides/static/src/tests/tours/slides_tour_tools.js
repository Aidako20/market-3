flectra.define('website_slides.tour.tools',function(require){
'usestrict';

/*
 *PUBLISHER/CONTENTCREATION
 */

varaddSection=function(sectionName){
	return[
{
    content:'eLearning:clickonAddSection',
    trigger:'a.o_wslides_js_slide_section_add',
},{
    content:'eLearning:setsectionname',
    trigger:'input[name="name"]',
    run:'text'+sectionName,
},{
    content:'eLearning:createsection',
    trigger:'footer.modal-footerbutton:contains("Save")'
},{
	content:'eLearning:sectioncreatedempty',
	trigger:'div.o_wslides_slide_list_category_header:contains("'+sectionName+'")',
}];
};

varaddVideoToSection=function(sectionName,saveAsDraft){
	varbase_steps=[
{
	content:'eLearning:addcontenttosection',
	trigger:'div.o_wslides_slide_list_category_header:contains("'+sectionName+'")a:contains("AddContent")',
},{
	content:'eLearning:clickonvideo',
	trigger:'a[data-slide-type=video]',
},{
	content:'eLearning:fillvideolink',
	trigger:'input[name=url]',
	run:'texthttps://www.youtube.com/watch?v=NvS351QKFV4&list=PLtVFNIekBzqIfO4u4n78i43etfw2n1St8&index=2&t=0s',
},{
    content:'eLearning:clickoutsidetotriggeronchange',
    trigger:'div.o_w_slide_upload_modal_container',
    run:'click',
}];
	if(saveAsDraft){
		base_steps=[].concat(base_steps,[{
    content:'eLearning:saveasdraftslide',
    extra_trigger:'div.o_slide_previewimg:not([src="/website_slides/static/src/img/document.png"])', //waitforonchangetoperformitsduty
    trigger:'footer.modal-footerbutton:contains("SaveasDraft")',
}]);
	}
	else{
		base_steps=[].concat(base_steps,[{
    content:'eLearning:createandpublishslide',
    extra_trigger:'div.o_slide_previewimg:not([src="/website_slides/static/src/img/document.png"])', //waitforonchangetoperformitsduty
    trigger:'footer.modal-footerbutton:contains("Publish")',
}]);
	}
	returnbase_steps;
};

varaddWebPageToSection=function(sectionName,pageName){
	return[
{
	content:'eLearning:addcontenttosection',
	trigger:'div.o_wslides_slide_list_category_header:contains("'+sectionName+'")a:contains("AddContent")',
},{
	content:'eLearning:clickonwebpage',
	trigger:'a[data-slide-type=webpage]',
},{
	content:'eLearning:fillwebpagetitle',
	trigger:'input[name=name]',
	run:'text'+pageName,
},{
    content:'eLearning:clickontags',
    trigger:'ul.select2-choices:first',
},{
    content:'eLearning:selectTheorytag',
    trigger:'div.select2-result-label:contains("Theory")',
    in_modal:false,
},{
	content:'eLearning:fillwebpagecompletiontime',
	trigger:'input[name=duration]',
	run:'text4',
}];
};

varaddExistingCourseTag=function(){
	return[
{
    content:'eLearning:clickonAddTag',
    trigger:'a.o_wslides_js_channel_tag_add',
},{
    content:'eLearning:clickontagdropdown',
    trigger:'a.select2-choice:first',
},{
    content:'eLearning:selectadvancedtag',
    trigger:'div.select2-result-label:contains("Advanced")',
    in_modal:false,
},{
    content:'eLearning:addexistingcoursetag',
    trigger:'footer.modal-footerbutton:contains("Add")'
}];
};

varaddNewCourseTag=function(courseTagName){
	return[
{
    content:'eLearning:clickonAddTag',
    trigger:'a.o_wslides_js_channel_tag_add',
},{
    content:'eLearning:clickontagdropdown',
	trigger:'a.select2-choice:first',
},{
    content:'eLearning:addanewcoursetag',
	trigger:'a.select2-choice:first',
	run:function(){
		//directlyaddnewtagsincewecanassumeselect2workscorrectly
		$('#tag_id').select2('data',{id:'123',text:courseTagName,create:true});
		$('#tag_id').trigger('change');
	}
},{
    content:'eLearning:clickontaggroupdropdown',
	trigger:'a.select2-choice:last',
},{
	content:'eLearning:selectTagstaggroup',
    trigger:'div.select2-result-label:contains("Tags")',
	in_modal:false,
},{
    content:'eLearning:addnewcoursetag',
    trigger:'footer.modal-footerbutton:contains("Add")'
}];
};

return{
	addSection:addSection,
	addVideoToSection:addVideoToSection,
	addWebPageToSection:addWebPageToSection,
	addExistingCourseTag:addExistingCourseTag,
	addNewCourseTag:addNewCourseTag,
};

});
